/**
 * Compares two bench result files (base vs head) and emits a markdown diff
 * for the PR sticky comment. Usage:
 *   node src/runner/compare.ts base.json head.json [--out comment.md] [--baseline perf.json]
 *
 * Run both sides on the same runner and in the same mode (--quick) so the
 * hardware is controlled; the diff then reflects the code/dependency change.
 *
 * Two independent signals are reported:
 *   1. base → head, measured back-to-back on the *same* runner. This isolates
 *      the effect of *this PR*. Because quick-mode numbers are noisy, the delta
 *      is computed on the median (p50) and only flagged when it exceeds both a
 *      minimum magnitude and the benchmarks' own measured jitter — so unrelated
 *      PRs stop surfacing phantom "regressions".
 *   2. head → committed baseline (baselines/perf.json), when present. The same-
 *      runner A/B only ever compares against the PR's immediate parent, so a
 *      small regression that merges becomes the new reference and hides. The
 *      committed baseline is a fixed, human-reviewed reference (regenerated by
 *      the perf-baseline workflow), so slow drift stays visible until someone
 *      deliberately re-blesses it. It's cross-run, so it uses a wider band and
 *      is advisory only.
 */
import { readFile, writeFile } from "node:fs/promises";
import type { BenchResultFile, BenchStats } from "../types.ts";

/** base→head: minimum relative change worth flagging at all. */
const FLAG_THRESHOLD = 0.15;
/** head→committed baseline: wider band, because the two were measured on
 * different runners at different times (cross-run hardware weather). */
const DRIFT_THRESHOLD = 0.25;

const [basePath, headPath] = process.argv.slice(2).filter((a) => !a.startsWith("--"));
const outIdx = process.argv.indexOf("--out");
const outPath = outIdx >= 0 ? process.argv[outIdx + 1] : undefined;
const baselineIdx = process.argv.indexOf("--baseline");
const baselinePath = baselineIdx >= 0 ? process.argv[baselineIdx + 1] : undefined;

if (!headPath) {
	console.error(
		"usage: node src/runner/compare.ts base.json head.json [--out file] [--baseline perf.json]",
	);
	process.exit(1);
}

async function load(file: string): Promise<BenchResultFile | undefined> {
	try {
		return JSON.parse(await readFile(file, "utf8")) as BenchResultFile;
	} catch {
		return undefined;
	}
}

const base = basePath ? await load(basePath) : undefined;
const head = await load(headPath);
const baseline = baselinePath ? await load(baselinePath) : undefined;
if (!head) {
	console.error(`cannot read head results: ${headPath}`);
	process.exit(1);
}

const lines: string[] = [];
lines.push("## 📊 Benchmark comparison");
lines.push("");
lines.push(
	`Runtime: ${head.runtime.name} ${head.runtime.version} · ${head.platform.cpu}${head.quick ? " · quick mode (indicative numbers)" : ""}`,
);
lines.push("");

const key = (b: { adapter: string; scenario: string; format: string }) =>
	`${b.adapter}|${b.scenario}|${b.format}`;
const name = (b: { adapter: string; scenario: string; format: string }) =>
	`${b.adapter} · ${b.scenario} · ${b.format}`;
const fmtMs = (ns: number) => `${(ns / 1e6).toPrecision(3)} ms`;
const pctOf = (delta: number) => `${delta > 0 ? "+" : ""}${(delta * 100).toFixed(1)}%`;

/**
 * Relative upper-quartile spread — a robust, outlier-resistant proxy for a
 * benchmark's run-to-run jitter. Raw samples aren't persisted, so this is
 * derived from the summary quartiles (p75 vs p50) rather than a true stddev.
 */
const relSpread = (s: BenchStats) => (s.p50Ns > 0 ? Math.max(0, (s.p75Ns - s.p50Ns) / s.p50Ns) : 0);

if (!base) {
	lines.push("_No base results available (new benchmark setup?) — head-only numbers below._");
	lines.push("");
	lines.push("| Benchmark | Median |");
	lines.push("| --- | --- |");
	for (const b of head.benchmarks) {
		lines.push(`| ${name(b)} | ${fmtMs(b.stats.p50Ns)} |`);
	}
	lines.push("");
} else {
	// Library version changes.
	const versionChanges: string[] = [];
	for (const [lib, info] of Object.entries(head.libraries)) {
		const baseInfo = base.libraries[lib];
		if (baseInfo && baseInfo.version !== info.version) {
			versionChanges.push(`- **${lib}**: \`${baseInfo.version}\` → \`${info.version}\``);
		}
	}
	if (versionChanges.length > 0) {
		lines.push("**Library version changes:**");
		lines.push(...versionChanges);
		lines.push("");
	}

	const baseMap = new Map(base.benchmarks.map((b) => [key(b), b]));
	const headMap = new Map(head.benchmarks.map((b) => [key(b), b]));

	interface Row {
		name: string;
		baseNs: number;
		headNs: number;
		delta: number;
		/** Combined measured jitter of both sides; deltas within it are noise. */
		noiseBand: number;
	}
	const rows: Row[] = [];
	for (const b of head.benchmarks) {
		const baseRow = baseMap.get(key(b));
		if (!baseRow) continue;
		rows.push({
			name: name(b),
			baseNs: baseRow.stats.p50Ns,
			headNs: b.stats.p50Ns,
			delta: (b.stats.p50Ns - baseRow.stats.p50Ns) / baseRow.stats.p50Ns,
			noiseBand: relSpread(baseRow.stats) + relSpread(b.stats),
		});
	}

	// A change is "interesting" only when it clears both a minimum magnitude and
	// the benchmarks' own quick-mode noise — a real regression shifts the whole
	// distribution and clears this; run-to-run jitter does not.
	const flagged = rows
		.filter((r) => Math.abs(r.delta) >= Math.max(FLAG_THRESHOLD, r.noiseBand))
		.toSorted((a, b) => a.delta - b.delta);
	const added = head.benchmarks.filter((b) => !baseMap.has(key(b)));
	const removed = base.benchmarks.filter((b) => !headMap.has(key(b)));

	lines.push("### This PR (base → head, same runner)");
	lines.push("");
	if (flagged.length === 0) {
		lines.push(
			`No benchmark moved beyond ±${FLAG_THRESHOLD * 100}% *and* its own quick-mode noise.`,
		);
		lines.push("");
	} else {
		lines.push("**Interesting changes** (beyond ±15% and the benchmark's own measured noise):");
		lines.push("");
		lines.push("| Benchmark | Base | Head | Δ (median) |");
		lines.push("| --- | --- | --- | --- |");
		for (const r of flagged) {
			const icon = r.delta < 0 ? "🚀" : "⚠️";
			lines.push(
				`| ${r.name} | ${fmtMs(r.baseNs)} | ${fmtMs(r.headNs)} | ${icon} ${pctOf(r.delta)} |`,
			);
		}
		lines.push("");
	}

	if (added.length > 0) {
		lines.push(`**Newly supported:** ${added.map(name).join(", ")}`);
		lines.push("");
	}
	if (removed.length > 0) {
		lines.push(`**No longer supported:** ${removed.map(name).join(", ")}`);
		lines.push("");
	}

	lines.push("<details><summary>All results (median)</summary>");
	lines.push("");
	lines.push("| Benchmark | Base | Head | Δ |");
	lines.push("| --- | --- | --- | --- |");
	for (const r of rows.toSorted((a, b) => a.name.localeCompare(b.name))) {
		lines.push(`| ${r.name} | ${fmtMs(r.baseNs)} | ${fmtMs(r.headNs)} | ${pctOf(r.delta)} |`);
	}
	lines.push("");
	lines.push("</details>");
	lines.push("");
}

// Drift vs the committed baseline — orthogonal to the base→head A/B: it catches
// slow regressions that accumulate across many merges, each individually below
// the A/B threshold, which the immediate-parent comparison can never see.
if (baseline) {
	const baselineMap = new Map(baseline.benchmarks.map((b) => [key(b), b]));
	interface Drift {
		name: string;
		refNs: number;
		headNs: number;
		delta: number;
	}
	const drifts: Drift[] = [];
	for (const b of head.benchmarks) {
		const ref = baselineMap.get(key(b));
		if (!ref) continue;
		const delta = (b.stats.p50Ns - ref.stats.p50Ns) / ref.stats.p50Ns;
		if (Math.abs(delta) >= DRIFT_THRESHOLD) {
			drifts.push({ name: name(b), refNs: ref.stats.p50Ns, headNs: b.stats.p50Ns, delta });
		}
	}
	drifts.sort((a, b) => b.delta - a.delta);

	lines.push(`### Drift vs committed baseline`);
	lines.push("");
	lines.push(
		`_Cross-run and advisory — compares head against \`${baselinePath}\` (captured ${baseline.timestamp}), a fixed reference that only moves when the perf-baseline workflow re-blesses it. Surfaces slow drift the same-runner A/B can't._`,
	);
	lines.push("");
	if (drifts.length === 0) {
		lines.push(
			`No benchmark drifted beyond ±${DRIFT_THRESHOLD * 100}% from the committed baseline.`,
		);
	} else {
		lines.push("| Benchmark | Baseline | Head | Δ |");
		lines.push("| --- | --- | --- | --- |");
		for (const d of drifts) {
			const icon = d.delta < 0 ? "🚀" : "⚠️";
			lines.push(
				`| ${d.name} | ${fmtMs(d.refNs)} | ${fmtMs(d.headNs)} | ${icon} ${pctOf(d.delta)} |`,
			);
		}
	}
	lines.push("");
}

const output = lines.join("\n");
if (outPath) {
	await writeFile(outPath, output);
	console.error(`comparison written to ${outPath}`);
} else {
	console.log(output);
}
